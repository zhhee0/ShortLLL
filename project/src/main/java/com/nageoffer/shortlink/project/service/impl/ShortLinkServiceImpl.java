    @SneakyThrows
    @Override
    public void restoreUrl(String shortUri, ServletRequest request, ServletResponse response) {
        // 短链接接口的并发量有多少？如何测试？详情查看：https://nageoffer.com/shortlink/question
        // 面试中如何回答短链接是如何跳转长链接？详情查看：https://nageoffer.com/shortlink/question
        HttpServletRequest httpRequest = (HttpServletRequest) request;
        String serverHost = resolveRequestHost(httpRequest);
        String primaryFullShortUrl = serverHost + "/" + shortUri;
        String fallbackFullShortUrl = buildFallbackFullShortUrl(serverHost, httpRequest.getServerPort(), shortUri);
        String fullShortUrl = primaryFullShortUrl;
        String originalLink = stringRedisTemplate.opsForValue().get(String.format(GOTO_SHORT_LINK_KEY, primaryFullShortUrl));
        if (StrUtil.isBlank(originalLink) && StrUtil.isNotBlank(fallbackFullShortUrl)) {
            originalLink = stringRedisTemplate.opsForValue().get(String.format(GOTO_SHORT_LINK_KEY, fallbackFullShortUrl));
            if (StrUtil.isNotBlank(originalLink)) {
                fullShortUrl = fallbackFullShortUrl;
            }
        }
        if (StrUtil.isNotBlank(originalLink)) {
            /**
             *  这里用消息队列是为了把“跳转”与“统计”解耦，避免跳转请求被统计逻辑拖慢或拖垮。
             *   - 性能：跳转是高频路径，统计写库/聚合是重操作。用 MQ 异步化可以把用户跳转耗时降到最低。
             *   - 削峰填谷：流量突增时，MQ 缓冲请求，消费者慢慢写库，避免数据库被打爆。
             *   - 解耦：跳转逻辑不依赖统计系统是否可用，统计挂了也不影响跳转。
             *   - 最终一致：统计允许延迟一致，用户感知不到。
             *    跳转必须快、统计允许慢，用 MQ 做异步削峰，保证高并发下依旧稳定。
             */
            shortLinkStats(buildLinkStatsRecordAndSetUser(fullShortUrl, request, response));
            sendRedirectSafely((HttpServletResponse) response, originalLink);
            return;
        }
        boolean contains = shortUriCreateCachePenetrationBloomFilter.contains(primaryFullShortUrl);
        if (!contains && StrUtil.isNotBlank(fallbackFullShortUrl)) {
            contains = shortUriCreateCachePenetrationBloomFilter.contains(fallbackFullShortUrl);
            if (contains) {
                fullShortUrl = fallbackFullShortUrl;
            }
        }
        if (!contains) {
            sendRedirectSafely((HttpServletResponse) response, "/page/notfound");
            return;
        }
        String gotoIsNullShortLink = stringRedisTemplate.opsForValue().get(String.format(GOTO_IS_NULL_SHORT_LINK_KEY, fullShortUrl));
        if (StrUtil.isNotBlank(gotoIsNullShortLink)) {
            sendRedirectSafely((HttpServletResponse) response, "/page/notfound");
            return;
        }
        RLock lock = redissonClient.getLock(String.format(LOCK_GOTO_SHORT_LINK_KEY, fullShortUrl));
        lock.lock();
        try {
            originalLink = stringRedisTemplate.opsForValue().get(String.format(GOTO_SHORT_LINK_KEY, fullShortUrl));
            if (StrUtil.isNotBlank(originalLink)) {
                shortLinkStats(buildLinkStatsRecordAndSetUser(fullShortUrl, request, response));
                sendRedirectSafely((HttpServletResponse) response, originalLink);
                return;
            }
            gotoIsNullShortLink = stringRedisTemplate.opsForValue().get(String.format(GOTO_IS_NULL_SHORT_LINK_KEY, fullShortUrl));
            if (StrUtil.isNotBlank(gotoIsNullShortLink)) {
                sendRedirectSafely((HttpServletResponse) response, "/page/notfound");
                return;
            }
            LambdaQueryWrapper<ShortLinkGotoDO> linkGotoQueryWrapper = Wrappers.lambdaQuery(ShortLinkGotoDO.class)
                    .eq(ShortLinkGotoDO::getFullShortUrl, fullShortUrl);
            ShortLinkGotoDO shortLinkGotoDO = shortLinkGotoMapper.selectOne(linkGotoQueryWrapper);
            if (shortLinkGotoDO == null) {
                stringRedisTemplate.opsForValue().set(String.format(GOTO_IS_NULL_SHORT_LINK_KEY, fullShortUrl), "-", 30, TimeUnit.MINUTES);
                sendRedirectSafely((HttpServletResponse) response, "/page/notfound");
                return;
            }
            LambdaQueryWrapper<ShortLinkDO> queryWrapper = Wrappers.lambdaQuery(ShortLinkDO.class)
                    .eq(ShortLinkDO::getGid, shortLinkGotoDO.getGid())
                    .eq(ShortLinkDO::getFullShortUrl, fullShortUrl)
                    .eq(ShortLinkDO::getDelFlag, 0)
                    .eq(ShortLinkDO::getEnableStatus, 0);
            ShortLinkDO shortLinkDO = baseMapper.selectOne(queryWrapper);
            if (shortLinkDO == null || (shortLinkDO.getValidDate() != null && shortLinkDO.getValidDate().before(new Date()))) {
                stringRedisTemplate.opsForValue().set(String.format(GOTO_IS_NULL_SHORT_LINK_KEY, fullShortUrl), "-", 30, TimeUnit.MINUTES);
                sendRedirectSafely((HttpServletResponse) response, "/page/notfound");
                return;
            }
            stringRedisTemplate.opsForValue().set(
                    String.format(GOTO_SHORT_LINK_KEY, fullShortUrl),
                    shortLinkDO.getOriginUrl(),
                    LinkUtil.getLinkCacheValidTime(shortLinkDO.getValidDate()), TimeUnit.MILLISECONDS
            );
            shortLinkStats(buildLinkStatsRecordAndSetUser(fullShortUrl, request, response));
            sendRedirectSafely((HttpServletResponse) response, shortLinkDO.getOriginUrl());
        } finally {
            lock.unlock();
        }
    }

    private ShortLinkStatsRecordDTO buildLinkStatsRecordAndSetUser(String fullShortUrl, ServletRequest request, ServletResponse response) {
        AtomicBoolean uvFirstFlag = new AtomicBoolean();
        Cookie[] cookies = ((HttpServletRequest) request).getCookies();
        AtomicReference<String> uv = new AtomicReference<>();
        Runnable addResponseCookieTask = () -> {
            uv.set(UUID.fastUUID().toString());
            Cookie uvCookie = new Cookie("uv", uv.get());
            uvCookie.setMaxAge(60 * 60 * 24 * 30);
            uvCookie.setPath(StrUtil.sub(fullShortUrl, fullShortUrl.indexOf("/"), fullShortUrl.length()));
            ((HttpServletResponse) response).addCookie(uvCookie);
            uvFirstFlag.set(Boolean.TRUE);
            stringRedisTemplate.opsForSet().add(SHORT_LINK_STATS_UV_KEY + fullShortUrl, uv.get());
        };
        if (ArrayUtil.isNotEmpty(cookies)) {
            Arrays.stream(cookies)
                    .filter(each -> Objects.equals(each.getName(), "uv"))
                    .findFirst()
                    .map(Cookie::getValue)
                    .ifPresentOrElse(each -> {
                        uv.set(each);
                        Long uvAdded = stringRedisTemplate.opsForSet().add(SHORT_LINK_STATS_UV_KEY + fullShortUrl, each);
                        uvFirstFlag.set(uvAdded != null && uvAdded > 0L);
                    }, addResponseCookieTask);
        } else {
            addResponseCookieTask.run();
        }
        HttpServletRequest httpRequest = (HttpServletRequest) request;
        String remoteAddr = LinkUtil.getActualIp(httpRequest);
        String os = LinkUtil.getOs(((HttpServletRequest) request));
        String browser = LinkUtil.getBrowser(((HttpServletRequest) request));
        String device = LinkUtil.getDevice(((HttpServletRequest) request));
        String network = LinkUtil.getNetwork(((HttpServletRequest) request));
        Double lat = parseLocationValue(httpRequest.getParameter("lat"), -90D, 90D);
        Double lng = parseLocationValue(httpRequest.getParameter("lng"), -180D, 180D);
        Long uipAdded = stringRedisTemplate.opsForSet().add(SHORT_LINK_STATS_UIP_KEY + fullShortUrl, remoteAddr);
        boolean uipFirstFlag = uipAdded != null && uipAdded > 0L;
        return ShortLinkStatsRecordDTO.builder()
                .fullShortUrl(fullShortUrl)
                .uv(uv.get())
                .uvFirstFlag(uvFirstFlag.get())
                .uipFirstFlag(uipFirstFlag)
                .remoteAddr(remoteAddr)
                .os(os)
                .browser(browser)
                .device(device)
                .network(network)
                .currentDate(new Date())
                .lat(lat)
                .lng(lng)
                .build();
    }

    private Double parseLocationValue(String value, double min, double max) {
        if (StrUtil.isBlank(value)) {
            return null;
        }
        try {
            double parsed = Double.parseDouble(value);
            if (parsed < min || parsed > max) {
                return null;
            }
            return parsed;
        } catch (NumberFormatException ignored) {
            return null;
        }
    }

    private String resolveRequestHost(HttpServletRequest request) {
        String forwardedHost = request.getHeader("X-Forwarded-Host");
        String host = StrUtil.isNotBlank(forwardedHost) ? forwardedHost : request.getHeader("Host");
        if (StrUtil.isNotBlank(host)) {
            return host.split(",")[0].trim();
        }
        String serverName = request.getServerName();
        int port = request.getServerPort();
        if (port == 80 || port == 443) {
            return serverName;
        }
        return serverName + ":" + port;
    }

    private String buildFallbackFullShortUrl(String serverHost, int port, String shortUri) {
        if (StrUtil.isBlank(serverHost) || serverHost.contains(":")) {
            return null;
        }
        if (port == 80 || port == 443) {
            return null;
        }
        return serverHost + ":" + port + "/" + shortUri;
    }

    private void sendRedirectSafely(HttpServletResponse response, String targetUrl) {
        String normalizedUrl = normalizeRedirectUrl(targetUrl);
        try {
            response.sendRedirect(normalizedUrl);
        } catch (Exception ex) {
            log.warn("Send redirect failed, fallback to html redirect. url={}", normalizedUrl, ex);
            renderRedirectFallback(response, normalizedUrl);
        }
    }

    private String normalizeRedirectUrl(String targetUrl) {
        if (StrUtil.isBlank(targetUrl)) {
            return targetUrl;
        }
        String normalized = targetUrl.trim();
        int hashIndex = normalized.indexOf('#');
        if (hashIndex < 0 || hashIndex == normalized.length() - 1) {
            return normalized;
        }
        String fragment = normalized.substring(hashIndex + 1);
        String encodedFragment = encodeFragmentPreserveEscapes(fragment);
        if (fragment.equals(encodedFragment)) {
            return normalized;
        }
        return normalized.substring(0, hashIndex + 1) + encodedFragment;
    }

    private void normalizeCreateRequest(ShortLinkCreateReqDTO requestParam) {
        if (requestParam == null) {
            return;
        }
        requestParam.setOriginUrl(normalizeOriginUrl(requestParam.getOriginUrl()));
        if (requestParam.getDescribe() != null) {
            requestParam.setDescribe(requestParam.getDescribe().trim());
        }
    }

    private String normalizeOriginUrl(String originUrl) {
        if (originUrl == null) {
            return null;
        }
        String normalized = originUrl.trim();
        if (normalized.indexOf('\r') >= 0 || normalized.indexOf('\n') >= 0) {
            normalized = normalized.replace("\r", "").replace("\n", "");
        }
        return normalized;
    }

    private String encodeFragmentPreserveEscapes(String fragment) {
        StringBuilder builder = new StringBuilder(fragment.length());
        int length = fragment.length();
        int i = 0;
        while (i < length) {
            char ch = fragment.charAt(i);
            if (ch == '%' && i + 2 < length && isHexDigit(fragment.charAt(i + 1)) && isHexDigit(fragment.charAt(i + 2))) {
                builder.append(ch).append(fragment.charAt(i + 1)).append(fragment.charAt(i + 2));
                i += 3;
                continue;
            }
            int codePoint = fragment.codePointAt(i);
            if (codePoint <= 0x7F && isFragmentSafeAscii((char) codePoint)) {
                builder.append((char) codePoint);
                i += 1;
                continue;
            }
            byte[] bytes = new String(Character.toChars(codePoint)).getBytes(java.nio.charset.StandardCharsets.UTF_8);
            for (byte b : bytes) {
                builder.append('%');
                String hex = Integer.toHexString(b & 0xFF).toUpperCase();
                if (hex.length() == 1) {
                    builder.append('0');
                }
                builder.append(hex);
            }
            i += Character.charCount(codePoint);
        }
        return builder.toString();
    }

    private boolean isHexDigit(char ch) {
        return (ch >= '0' && ch <= '9')
                || (ch >= 'a' && ch <= 'f')
                || (ch >= 'A' && ch <= 'F');
    }

    private boolean isFragmentSafeAscii(char ch) {
        return (ch >= 'a' && ch <= 'z')
                || (ch >= 'A' && ch <= 'Z')
                || (ch >= '0' && ch <= '9')
                || ch == '-' || ch == '_' || ch == '.' || ch == '~'
                || ch == '!' || ch == '$' || ch == '&' || ch == '\'' || ch == '(' || ch == ')' || ch == '*'
                || ch == '+' || ch == ',' || ch == ';' || ch == '='
                || ch == ':' || ch == '@' || ch == '/' || ch == '?';
    }

    private void renderRedirectFallback(HttpServletResponse response, String targetUrl) {
        try {
            String escapedUrl = HtmlUtils.htmlEscape(targetUrl);
            String jsEscapedUrl = escapeForJsString(targetUrl);
            response.setStatus(302);
            response.setContentType("text/html;charset=UTF-8");
            response.getWriter().write("""
                    <!doctype html>
                    <html lang="en">
                      <head>
                        <meta charset="utf-8" />
                        <meta http-equiv="refresh" content="0;url=%s" />
                        <title>Redirecting...</title>
                      </head>
                      <body>
                        <p>Redirecting to <a href="%s">%s</a></p>
                        <script>
                          window.location.replace("%s");
                        </script>
                      </body>
                    </html>
                    """.formatted(escapedUrl, escapedUrl, escapedUrl, jsEscapedUrl));
        } catch (Exception fallbackEx) {
            log.warn("Render redirect fallback failed. url={}", targetUrl, fallbackEx);
        }
    }

    private String escapeForJsString(String value) {
        if (value == null) {
            return "";
        }
        StringBuilder builder = new StringBuilder(value.length());
        for (int i = 0; i < value.length(); i++) {
            char ch = value.charAt(i);
            switch (ch) {
                case '\\':
                    builder.append("\\\\");
                    break;
                case '"':
                    builder.append("\\\"");
                    break;
                case '\n':
                    builder.append("\\n");
                    break;
                case '\r':
                    builder.append("\\r");
                    break;
                case '\t':
                    builder.append("\\t");
                    break;
                case '\u2028':
                    builder.append("\\u2028");
                    break;
                case '\u2029':
                    builder.append("\\u2029");
                    break;
                default:
                    builder.append(ch);
            }
        }
        return builder.toString();
    }

