    private String normalizeLocaleName(String locale) {
        if (StrUtil.isBlank(locale) || StrUtil.equals(locale, "[]")) {
            return "未知";
        }
        return locale;
    }

    private double[] resolveCityCenter(String adcode, String province, String city) {
        List<String> keywords = buildDistrictKeywords(adcode, province, city);
        for (String keyword : keywords) {
            String cacheKey = "district:" + keyword;
            double[] cached = cityCenterCache.get(cacheKey);
            if (cached != null) {
                return cached;
            }
            if (cityCenterMissing.contains(cacheKey)) {
                continue;
            }
            double[] center = fetchCityCenter(keyword);
            if (center == null) {
                cityCenterMissing.add(cacheKey);
                continue;
            }
            cityCenterCache.put(cacheKey, center);
            return center;
        }
        return null;
    }

    private List<String> buildDistrictKeywords(String adcode, String province, String city) {
        List<String> keywords = new ArrayList<>();
        String normalizedAdcode = normalizeLocaleName(adcode);
        String normalizedProvince = normalizeLocaleName(province);
        String normalizedCity = normalizeLocaleName(city);
        if (StrUtil.isNotBlank(normalizedAdcode) && !StrUtil.equals(normalizedAdcode, "未知")) {
            if (!isProvinceAdcode(normalizedAdcode)) {
                keywords.add(normalizedAdcode);
            }
        }
        if (StrUtil.isNotBlank(normalizedCity) && !StrUtil.equals(normalizedCity, "未知")) {
            keywords.add(normalizedCity);
            if (StrUtil.isNotBlank(normalizedProvince) && !StrUtil.equals(normalizedProvince, "未知")) {
                keywords.add(normalizedProvince + normalizedCity);
            }
        }
        if (StrUtil.isNotBlank(normalizedAdcode) && !StrUtil.equals(normalizedAdcode, "未知")
                && isProvinceAdcode(normalizedAdcode)) {
            keywords.add(normalizedAdcode);
        }
        if (StrUtil.isNotBlank(normalizedProvince) && !StrUtil.equals(normalizedProvince, "未知")) {
            keywords.add(normalizedProvince);
        }
        return keywords;
    }

    private boolean isProvinceAdcode(String adcode) {
        return adcode.length() >= 6 && adcode.endsWith("0000");
    }

    private double[] fetchCityCenter(String keyword) {
        if (StrUtil.isBlank(statsLocaleAmapKey) || StrUtil.isBlank(keyword)) {
            return null;
        }
        Map<String, Object> paramMap = new HashMap<>();
        paramMap.put("key", statsLocaleAmapKey);
        paramMap.put("keywords", keyword);
        paramMap.put("subdistrict", 0);
        paramMap.put("extensions", "base");
        String result = HttpUtil.get(AMAP_DISTRICT_URL, paramMap);
        if (StrUtil.isBlank(result)) {
            return null;
        }
        JSONObject resultObj = JSON.parseObject(result);
        if (!StrUtil.equals(resultObj.getString("infocode"), "10000")) {
            return null;
        }
        JSONArray districts = resultObj.getJSONArray("districts");
        if (districts == null || districts.isEmpty()) {
            return null;
        }
        JSONObject district = districts.getJSONObject(0);
        String center = district.getString("center");
        if (StrUtil.isBlank(center)) {
            return null;
        }
        String[] parts = center.split(",");
        if (parts.length != 2) {
            return null;
        }
        try {
            return new double[]{Double.parseDouble(parts[0]), Double.parseDouble(parts[1])};
        } catch (NumberFormatException ignored) {
            return null;
        }
    }
